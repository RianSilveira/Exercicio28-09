public class Primos {
    public static void main(String[] args) {
        // Usando um laço for para iterar de 1 a 100
        for (int i = 1; i <= 100; i++) {
            // Verifica se o número é par
            if (i % 2 == 0) {
                System.out.println(i);
            }
        }
    }
}
